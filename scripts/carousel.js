    function getCarouselItem(place_name){
        template = `<div class="col-md-4 mb-3">
             <div class="card">
                 <img class="img-fluid" alt="100%x280"
                      src="${place_to_url_map[place_name.toLowerCase()]}">
                 <div class="card-body">
                     <h4 class="card-title">${place_name}</h4>
                 </div>
             </div>
         </div>`
         return template
    }

    function getCarouselRow(places_list, is_active){
        template = ""
        for (place in places_list){
            template += getCarouselItem(places_list[place]);
        }
        if (is_active){
            return `<div class="carousel-item active"> <div class="row">${template} </div></div>`
        }
        else{
            return `<div class="carousel-item"> <div class="row">${template} </div></div>`
        }

        return template
    }

    function getCarousel(places_lists){
        row_template = ""
        for (let i=0; i<places_lists.length; i++){
            if (i==0){
                row_template += getCarouselRow(places_lists[i], true);
            }
            else{
                row_template += getCarouselRow(places_lists[i], false);
            }
        }
        return ` <div id="carouselExampleIndicators2" class="carousel slide" data-ride="carousel">
                            <div class="carousel-inner">
                                   ${row_template}
                            </div>
                        </div>
                       `
    }
